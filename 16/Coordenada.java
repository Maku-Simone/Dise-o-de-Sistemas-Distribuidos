import java.io.Serializable;

public class Coordenada implements Serializable{
	public double x;
	public double y;

	public Coordenada(double x, double y) {
		this.x = x;
		this.y = y;
	}

	public double getX() {
		return x;
	}

	public double getY() {
		return y;
	}

	public void setX(double x) {
		this.x = x;
	}

	public void setY(double y) {
		this.y = y;
	}
}